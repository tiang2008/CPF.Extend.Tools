namespace CPF.Extend.Tools.Controls.ScreenCut
{
    /// <summary>
    /// 表示截图操作时鼠标的类型。
    /// </summary>
    public enum ScreenCutMouseType
    {
        /// <summary>
        /// 默认类型。
        /// </summary>
        Default,

        /// <summary>
        /// 绘制鼠标。
        /// </summary>
        DrawMouse,

        /// <summary>
        /// 移动鼠标。
        /// </summary>
        MoveMouse,

        /// <summary>
        /// 绘制矩形。
        /// </summary>
        DrawRectangle,

        /// <summary>
        /// 绘制椭圆。
        /// </summary>
        DrawEllipse,

        /// <summary>
        /// 绘制箭头。
        /// </summary>
        DrawArrow,

        /// <summary>
        /// 绘制文本。
        /// </summary>
        DrawText,

        /// <summary>
        /// 绘制墨水。
        /// </summary>
        DrawInk
    }
}
